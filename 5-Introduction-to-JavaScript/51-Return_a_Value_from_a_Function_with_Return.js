// The return statement acts similar to a console.log(). A return statement actually shows on the client side, while a console.log() shows in dev tools.

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line

function timesFive (num) {
  return num * 5
}

console.log(timesFive(3))
console.log(minusSeven(10));
