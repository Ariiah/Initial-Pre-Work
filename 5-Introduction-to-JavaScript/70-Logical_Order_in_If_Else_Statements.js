// To make the If Else statement work, the most complicated step needs to go first, otherwise, the first step may pass through without checking the other conditions.

function orderMyLogic(val) {
  if (val < 5) {
    return "Less than 5";
  } else if (val < 10) {
    return "Less than 10";
  } else {
    return "Greater than or equal to 10";
  }
}

// Change this value to test
orderMyLogic(7);
